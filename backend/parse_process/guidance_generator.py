#!/usr/bin/env python3
"""
guidance_generator.py — Post-Decision Guidance & Resource Finder
==================================================================
Runs AFTER an application's outcome is known (your tracker script calls
this with the outcome). Produces a personalized, actionable guide using a
local SLM, plus a fresh resource search via scraping:

  ACCEPTED              → "make the most of this" improvement guide +
                           complementary resources (financial literacy,
                           skill-building, related support).
  REJECTED /
  NO_PROGRAMS_FOUND     → "next steps" guide (incl. reapplication tips
                           where relevant) + a search for NGOs, charities,
                           and community/mutual-aid resources.

Everything runs locally except the optional web-search step, which only
ever sends generic category/location search terms — never personal
applicant details.

Intended call pattern (matches an external tracker script)
------------------------------------------------------------
  from guidance_generator import generate_guidance

  result = generate_guidance(
      program_applied_to=program,            # dict or name string
      application_result=outcome,            # "accepted" | "rejected" | "no_programs_found"
      previous_chat_profile=profile_or_report,
      conversations=[chat1_text, chat2_text],  # optional, multiple chats
  )

CLI usage
---------
  python guidance_generator.py accepted \\
      --program report.json --profile applicant_profile.json \\
      --conversation-file chat1.txt -o guide.md

  python guidance_generator.py rejected \\
      --program '{"name": "Illinois Family Food Support Program"}' \\
      --profile - < applicant_profile.json

Options
-------
  application_result          accepted | rejected | no_programs_found (free text OK)
  --program PATH_or_JSON_or_NAME   Program applied to (file, JSON string, or plain name)
  --profile PATH_or_-          Previous chat profile / assistance_finder report (default: stdin)
  --conversation-file PATH     Conversation transcript file (repeatable)
  --conversation-text TEXT     Conversation transcript inline text (repeatable)
  --location STATE             Override/seed location
  --backend / --host / --model / --temperature / --max-tokens / --timeout
                                Same as process_parse.py / assistance_finder.py
  --no-web-search               Disable the resource-search step
  --max-resources N             Cap on scraped resources (default: 4)
  -o, --output PATH             Save report to file
  --output-format markdown|json (default: markdown)
  --quiet / -q
"""

